package no.stonedstonar.wargames.model;

/**
 * An enum that represents the weight class of the different weapons.
 * @author Steinar Hjelle Midthus
 * @version 0.1
 */
public enum WeaponWeight {

    /**
     * Represents the heavy weapons like launchers.
     */
    HEAVY,

    /**
     * Represents the medium weapons like rifles or crossbows.
     */
    MEDIUM,

    /**
     * Represents light weapons like bows or handguns.
     */
    LIGHT
}
