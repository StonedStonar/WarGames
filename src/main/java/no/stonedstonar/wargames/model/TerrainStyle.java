package no.stonedstonar.wargames.model;

/**
 * An enum that represents different terrain styles.
 * @author Steinar Hjelle Midthus
 * @version 0.1
 */
public enum TerrainStyle {

    /**
     * Represents a hill as a terrain.
     */
    HILL,

    /**
     * Represents a forest as a terrain.
     */
    FOREST,

    /**
     * Represents plains as a terrain.
     */
    PLAINS
}
